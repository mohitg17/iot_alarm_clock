void HourHandSetup() {
	for(int i = 0; i < 12; i++) {
		struct Line Hand;
		Hand.x = (hourXPositions[i]*6)+63;
		Hand.y = (hourYPositions[i]*6)+64;
		HourHandPositions[i] = Hand;
	}
}

void MinuteHandSetup() {
	for(int i = 0; i < 60; i++) {
		struct Line Hand;
		Hand.x = (minuteXPositions[i]*2)+63;
		Hand.y = (minuteYPositions[i]*2)+64;
		MinuteHandPositions[i] = Hand;
	}
}

void drawStopwatch(uint8_t Hour, uint16_t Minute, uint16_t Second){
	ST7735_SetCursor(7, 11);
	uint32_t hour = (uint32_t)Hour;

	if(hour < 10) {
		ST7735_OutUDec(0);
	}
	ST7735_OutUDec(hour);
	ST7735_OutChar(':');
	if(Minute < 10) {
		ST7735_OutUDec(0);
	}
	ST7735_OutUDec(Minute);
	ST7735_OutChar(':');
	if(Second < 10) {
		ST7735_OutUDec(0);
	}
	ST7735_OutUDec(Second);
}

void drawDigital(uint8_t Hour, uint16_t Minute){
	ST7735_SetCursor(7, 11);
	ST7735_OutString("  ");
	uint32_t hour = (uint32_t)Hour;
	if(hour == 0) {
		hour = 12;
	}
	if(hour < 10) {
		ST7735_OutUDec(0);
	}
	ST7735_OutUDec(hour);
	ST7735_OutChar(':');
	if(Minute < 10) {
		ST7735_OutUDec(0);
	}
	ST7735_OutUDec(Minute);
	ST7735_OutString(" ");
}

